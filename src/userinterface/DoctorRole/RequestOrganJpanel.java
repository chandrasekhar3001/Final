    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean check;
        check=(cb_ped.getSelectedIndex()>0&&cb_age.getSelectedIndex()>0&&cb_reqOrgan.getSelectedIndex()>0&&cb_survival.getSelectedIndex()>0&&cb_urgency.getSelectedIndex()>0);
        if(check == false) {
            JOptionPane.showMessageDialog(null, "Please provide all parameters");
            return;
        }
        
        
        int dup=0;
        for(Wait w: ecoSystem.getWaitList()){
            if(w.getPatient().getId()==p.getId()&&w.getOrgan().equalsIgnoreCase(cb_reqOrgan.getSelectedItem().toString())){
               dup++; 
            }
        }
        
        check=(dup==0);
        if(!check){
            JOptionPane.showMessageDialog(null, "Patient is already in waitlist");
            return;
        }
        

        //Patient patient, String age, String ped, String survival, String urgency, String organ, int waitlist
        double score=0.0;
        if(ecoSystem.getWaitList().size()<1){
            score=calculateScore();
        }else{
            for(Wait w: ecoSystem.getWaitList()){
                if(w.getOrgan().equalsIgnoreCase(cb_reqOrgan.getSelectedItem().toString())&&w.getWaitlist()==calculateScore()){
                    score=calculateScore()+(ecoSystem.getWaitList().size()/10);
                    break;
                }else{
                    score=calculateScore();
                }
            }
        
        }
        
        JOptionPane.showMessageDialog(null, score);
        
        Wait w=new Wait(p,cb_age.getSelectedItem().toString(),cb_ped.getSelectedItem().toString(),cb_survival.getSelectedItem().toString(),cb_urgency.getSelectedItem().toString(),cb_reqOrgan.getSelectedItem().toString(),score,0);
        ecoSystem.addWaitlist(w);
        
        
        
        
        //JPanel userProcessContainer, UserAccount account, Medical organization, Enterprise enterprise, EcoSystem ecosystem, Network network
        DoctorWorkAreaJPanel rlt=new DoctorWorkAreaJPanel(userProcessContainer, userAccount,(Medical) org,enterprise,ecoSystem,network);
        rlt.initwaitTable();
        userProcessContainer.add("Lab test",rlt);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

